
public class _04_Constantes {
	public static void main(String[] args) {
		// Las constantes en java es un tipo especial de "variable"
		// el cual el valor que almacenenmos NO se puede cambiar
		// durante todo el ciclo de vida del programa
		
		// Las constantes pueden ser de cualquier tipo de dato
		// Las constantes en java se declaran con la palabra reservada
		// "Final"
		
		final double NUMERO_PI = 3.1416;
		
		// Las constantes en java se suelen declaran er UPPERSNAKECASE
		// Ej:CONSTANTE_NUMERICA
		System.out.println(NUMERO_PI);
		
		// Si intentamos el valor de una constante java nos dara
		// un error en tiempo de compilaci√≥n
		
		//NUMERO_PI = 3.6565; Esto da error
		
		//Podemos declarar constantes de lo que queramos
		
		final String TITULO_WEB = "bienvenidos a mi pagina web";
		
		
		
		
		
		
		
		
		
	}

}
