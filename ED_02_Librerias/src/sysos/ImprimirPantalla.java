package sysos;

public class ImprimirPantalla {
	
	/**
	 * Método que imprime por pantalla <b> Consola : </b> y luego
	 * concatena el valor pasado por parámetro.
	 * @param s la cadena a imprimir
	 */
	public void imprimir (String s) {
		System.out.println("Consola: " + s);
	}
	/**
	 * Método que imprime por pantalla <b> Consola : </b> y luego
	 * concatena el valor pasado por parámetro.
	 * @param n el entero a imprimir
	 */
	public void imprimir (int n) {
		System.out.println("Consola: " + n);
	}
}
